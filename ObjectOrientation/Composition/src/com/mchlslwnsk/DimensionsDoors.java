package com.mchlslwnsk;

public class DimensionsDoors {

    private int height;
    private int width;

    public DimensionsDoors(int height, int width) {
        this.height = height;
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }
}
